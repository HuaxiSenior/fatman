package Model;

public class Money {
	private Double chengben;
	private Double shouru;
	private Double yinli;
	public Double getChengben() {
		return chengben;
	}
	public void setChengben(Double chengben) {
		this.chengben = chengben;
	}
	public Double getShouru() {
		return shouru;
	}
	public void setShouru(Double shouru) {
		this.shouru = shouru;
	}
	public Double getYinli() {
		return yinli;
	}
	public void setYinli(Double yinli) {
		this.yinli = yinli;
	}
	
}
